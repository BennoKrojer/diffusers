def get_full_repo_name(model_id: str, organization: Optional[str] = None, token: Optional[str] = None):
    if token is None:
        token = HfFolder.get_token()
    if organization is None:
        username = whoami(token)["name"]
        return f"{username}/{model_id}"
    else:
        return f"{organization}/{model_id}"

def score_batch(i, args, batch, unet, vae, text_encoder, mid_mlp, noise_scheduler, weight_dtype):
    """
    Takes a batch of images and captions and returns a score for each image-caption pair.
    """
    imgs, text = batch[0], batch[1]
    if type(imgs) == list:
        imgs, imgs_resize = imgs[0], imgs[1]
        imgs, imgs_resize = [img.cuda() for img in imgs], [img.cuda() for img in imgs_resize]
    else:
        imgs = [img.cuda() for img in imgs]
        imgs_resize = imgs

    scores = [[], [], [], [], []]
    for img_idx, resized_img in enumerate(imgs_resize):
        if len(resized_img.shape) == 3:
            resized_img = resized_img.unsqueeze(0)
        
        latents = vae.encode(resized_img.to(dtype=weight_dtype)).latent_dist.sample()
        latents = latents * vae.config.scaling_factor

        noise = torch.randn_like(latents)
        encoder_hidden_states = text_encoder(text)[0]
        bsz = latents.shape[0]
        for i, noise_level in enumerate([0.5, 0.4, 0.3, 0.2, 0.1]):
            # Sample a random timestep for each image
            timesteps = torch.randint(int(noise_level * noise_scheduler.num_train_timesteps), int(noise_level * noise_scheduler.num_train_timesteps)+1, (bsz,), device=latents.device)
            timesteps = timesteps.long()

            # Add noise to the latents according to the noise magnitude at each timestep
            # (this is the forward diffusion process)
            noisy_latents = noise_scheduler.add_noise(latents, noise, timesteps)
            # Get the text embedding for conditioning

            # Predict the noise residual and compute loss
            mid_vec = unet(noisy_latents, timesteps, encoder_hidden_states)
            score = mid_mlp(mid_vec)
            scores[i].append(score)
                            
    # scores = torch.stack(scores).squeeze().unsqueeze(0)
    scores = [torch.stack(score).squeeze().unsqueeze(0) for score in scores]
    return scores

def main():
    args = parse_args()
    logging_dir = os.path.join(args.output_dir, args.logging_dir)

    accelerator_project_config = ProjectConfiguration(total_limit=args.checkpoints_total_limit)
    ddp_kwargs = DistributedDataParallelKwargs(find_unused_parameters=True)
    accelerator = Accelerator(
        gradient_accumulation_steps=args.gradient_accumulation_steps,
        mixed_precision=args.mixed_precision,
        log_with=args.report_to,
        logging_dir=logging_dir,
        project_config=accelerator_project_config,
        kwargs_handlers=[ddp_kwargs]
    )
    if args.report_to == "wandb":
        if not is_wandb_available():
            raise ImportError("Make sure to install wandb if you want to use it for logging during training.")
        import wandb
        wandb.init(project=f"diffusers_finetuning_{args.task}_scoring", settings=wandb.Settings(start_method="fork"))


    # Make one log on every process with the configuration for debugging.
    logging.basicConfig(
        format="%(asctime)s - %(levelname)s - %(name)s - %(message)s",
        datefmt="%m/%d/%Y %H:%M:%S",
        level=logging.INFO,
    )
    logger.info(accelerator.state, main_process_only=False)
    if accelerator.is_local_main_process:
        transformers.utils.logging.set_verbosity_warning()
        diffusers.utils.logging.set_verbosity_info()
    else:
        transformers.utils.logging.set_verbosity_error()
        diffusers.utils.logging.set_verbosity_error()

    # If passed along, set the training seed now.
    if args.seed is not None:
        set_seed(args.seed)

    # Handle the repository creation
    if accelerator.is_main_process:
        if args.push_to_hub:
            if args.hub_model_id is None:
                repo_name = get_full_repo_name(Path(args.output_dir).name, token=args.hub_token)
            else:
                repo_name = args.hub_model_id
            repo_name = create_repo(repo_name, exist_ok=True)
            repo = Repository(args.output_dir, clone_from=repo_name)

            with open(os.path.join(args.output_dir, ".gitignore"), "w+") as gitignore:
                if "step_*" not in gitignore:
                    gitignore.write("step_*\n")
                if "epoch_*" not in gitignore:
                    gitignore.write("epoch_*\n")
        elif args.output_dir is not None:
            os.makedirs(args.output_dir, exist_ok=True)

    # Load scheduler, tokenizer and models.
    noise_scheduler = DDPMScheduler.from_pretrained(args.pretrained_model_name_or_path, subfolder="scheduler")
    tokenizer = CLIPTokenizer.from_pretrained(
        args.pretrained_model_name_or_path, subfolder="tokenizer", revision=args.revision
    )
    text_encoder = CLIPTextModel.from_pretrained(
        args.pretrained_model_name_or_path, subfolder="text_encoder", revision=args.revision
    )
    vae = AutoencoderKL.from_pretrained(args.pretrained_model_name_or_path, subfolder="vae", revision=args.revision)
    unet = UNet2DConditionModel.from_pretrained(
        args.pretrained_model_name_or_path, subfolder="unet_finetuned", revision=args.revision, scoring_mode=True)
    # freeze parameters of models to save more memory
    # unet.requires_grad_(False)
    vae.requires_grad_(False)
    text_encoder.requires_grad_(False)

    mid_mlp = torch.nn.Sequential(
                torch.nn.Linear(81920, 1024),
                torch.nn.ReLU(),
                torch.nn.Linear(1024, 1))

    # load weights for mlp
    mid_mlp.load_state_dict(torch.load(os.path.join(args.pretrained_model_name_or_path, "mid_mlp", "pytorch_model_1.bin")))

    # For mixed precision training we cast the text_encoder and vae weights to half-precision
    # as these models are only used for inference, keeping weights in full precision is not required.
    weight_dtype = torch.float32
    if accelerator.mixed_precision == "fp16":
        weight_dtype = torch.float16
    elif accelerator.mixed_precision == "bf16":
        weight_dtype = torch.bfloat16

    # Move unet, vae and text_encoder to device and cast to weight_dtype
    unet.to(accelerator.device, dtype=weight_dtype)
    vae.to(accelerator.device, dtype=weight_dtype)
    text_encoder.to(accelerator.device, dtype=weight_dtype)
    mid_mlp.to(accelerator.device, dtype=weight_dtype)

    # now we will add new LoRA weights to the attention layers
    # It's important to realize here how many attention weights will be added and of which sizes
    # The sizes of the attention layers consist only of two different variables:
    # 1) - the "hidden_size", which is increased according to `unet.config.block_out_channels`.
    # 2) - the "cross attention size", which is set to `unet.config.cross_attention_dim`.

    # Let's first see how many attention processors we will have to set.
    # For Stable Diffusion, it should be equal to:
    # - down blocks (2x attention layers) * (2x transformer layers) * (3x down blocks) = 12
    # - mid blocks (2x attention layers) * (1x transformer layers) * (1x mid blocks) = 2
    # - up blocks (2x attention layers) * (3x transformer layers) * (3x down blocks) = 18
    # => 32 layers

    # Set correct lora layers
    # Enable TF32 for faster training on Ampere GPUs,
    # cf https://pytorch.org/docs/stable/notes/cuda.html#tensorfloat-32-tf32-on-ampere-devices
    if args.allow_tf32:
        torch.backends.cuda.matmul.allow_tf32 = True

    if args.scale_lr:
        args.learning_rate = (
            args.learning_rate * args.gradient_accumulation_steps * args.train_batch_size * accelerator.num_processes
        )

    # Get the datasets: you can either provide your own training and evaluation files (see below)
    # or specify a Dataset from the hub (the dataset will be downloaded automatically from the datasets Hub).

    # In distributed training, the load_dataset function guarantees that only one local process can concurrently
    # download the dataset.

    val_dataset = get_dataset(f'{args.task}', f'datasets/{args.task}', transform=None, split='val', tokenizer=tokenizer)
    val_dataloader = torch.utils.data.DataLoader(val_dataset, batch_size=1, shuffle=False, num_workers=0)
    # val_train_dataset = get_dataset(f'{args.task}', f'datasets/{args.task}', transform=preprocess, split='train')
    # val_train_dataloader = torch.utils.data.DataLoader(val_train_dataset, batch_size=1, shuffle=False, num_workers=0)

    unet, mid_mlp = accelerator.prepare(unet, mid_mlp)
    val_dataloader = accelerator.prepare(val_dataloader)

    if accelerator.is_main_process:
        r1s = defaultdict(list)
        r5s = defaultdict(list)
        for k, batch in tqdm(enumerate(val_dataloader), total=len(val_dataloader)):
            # if k % 5 != 0:
            #     continue
            # if k > 1000:
            #     break
            with torch.no_grad():
                scores = score_batch(k, args, batch, unet, vae, text_encoder, mid_mlp, noise_scheduler, weight_dtype)
            for scores_, noise_level in zip(scores,[0.5, 0.4, 0.3, 0.2, 0.1]):
                r1,r5 = evaluate_scores(args, scores_, batch)
                r1s[noise_level].append(r1)
                r5s[noise_level].append(r5)
                
                r1 = np.mean(r1s[noise_level])
                r5 = np.mean(r5s[noise_level])
                print(f"Noise level {noise_level}: R@1: {r1}, R@5: {r5}")
        # wandb.log({"Validation R@1": r1, "Validation R@5": r5, "step": global_step})

if __name__ == "__main__":
    main()